public class DoWhileDemo
{
	public static void main(String args[])
	{
		int count = 1;
		do
		{
			System.out.println("Good Morning");
			count++; // count = count + 1;
		}
		while(count <= 5);
		
	}
}
class DoWhile
{
	public static void main(String args[])
	{
		int a = 1;
		do
		{
			System.out.println("Good Night");
			a++;
		}
		while(a <= 5);
	}
}	